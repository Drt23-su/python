# Declarar las variables
numero_entero = 42              # Un número entero (int)
numero_decimal = 3.14          # Un número decimal (float)
cadena_texto = "Hola"          # Una cadena de texto (str)
valor_booleano = True           # Un valor booleano (bool)

# Imprimir el valor y el tipo de cada variable
print(f"Valor: {numero_entero}, Tipo: {type(numero_entero)}")
print(f"Valor: {numero_decimal}, Tipo: {type(numero_decimal)}")
print(f"Valor: {cadena_texto}, Tipo: {type(cadena_texto)}")
print(f"Valor: {valor_booleano}, Tipo: {type(valor_booleano)}")